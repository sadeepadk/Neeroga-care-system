     private void loadInvoiceItems() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        total = 0;

        for (BillItem billItem : invoiceItemMap.values()) {
            Vector<String> vector = new Vector<>();
            vector.add(billItem.getDoctor());
            vector.add(billItem.getChanelFee());
            vector.add(billItem.getFee());
            vector.add(billItem.getService());
            //vector.add(billItem.getTotal());
           
            double itemTotal = Double.parseDouble(billItem.getChanelFee()) + Double.parseDouble(billItem.getFee()) + Double.parseDouble(billItem.getService());
            total += itemTotal;
            vector.add(String.valueOf(itemTotal));

            model.addRow(vector);
        }

        jFormattedTextField4.setText(String.valueOf(total));

        //calculate();

    }
